#include "minishell.h"

void	start_shell(void)
{
	char	*str;

	while (1)
	{
		str = readline("╰(°▽°)╯~ ");
		if (!str || !ft_strncmp(str, "exit", 5))
			break ;
		printf("%s\n", str);
	}
}